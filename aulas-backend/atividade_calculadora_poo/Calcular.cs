namespace atividade_calculadora_poo
{
    public class calcular
    {
        public float n1;
        public float n2;

        public float Somar()
        {
            return this.n1 + this.n2;
        }
        public float Multiplicar()
        {
            return this.n1 * this.n2;
        }
        public float Subtrair()
        {
            return this.n1 - this.n2;
        }
        public float Dividir()
        {
            return this.n1 / this.n2;
        }
       
    }
}